import math

print('''Yo, –±—Ä–æ! 
–°–∞–ª—é—Ç! 

–ö–∞–∫ —Å–∞–º? 
–°–ø–∞—Å—ë—à—å —É—Ç–æ–ø–∞—é—â–µ–≥–æ!? 
–ò–ª–∏ "–°–ø–∞—Å–µ–Ω–∏–µ —É—Ç–æ–ø–∞—é—â–∏—Ö-–¥–µ–ª–æ —Ä—É–∫ —Å–∞–º–∏—Ö —É—Ç–æ–ø–∞—é—â–∏—Ö?"''')
print()

# –ó–∞–¥–∞—ë–º –∑–Ω–∞—á–µ–Ω–∏–µ d1 (–≤ —è—Ä–¥–∞—Ö) –∏ tan1 (–≤ –≥—Ä–∞–¥—É—Å–∞—Ö)
d1 = input('–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ d1 –≤ —è—Ä–¥–∞—Ö: ') # –ù–∞–¥–æ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å —è—Ä–¥—ã –≤ —Ñ—É—Ç—ã!!!
d1 = float(d1) # –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –≤ —á–∏—Å–ª–æ —Å –ø–ª–∞–≤–∞—é—â–µ–π —Ç–æ—á–∫–æ–π
d1 = d1 * 3
print() # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ </br>
tan1 = float(input('–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ ùúÉ1 –≤ –≥—Ä–∞–¥—É—Å–∞—Ö: ')) # –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –≤ —á–∏—Å–ª–æ —Å –ø–ª–∞–≤–∞—é—â–µ–π —Ç–æ—á–∫–æ–π
print()

# –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω–æ–≥–æ –≤ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
tan1 = int(round(tan1))  # –û–∫—Ä—É–≥–ª–µ–Ω–∏–µ –¥–æ –±–ª–∏–∂–∞–π—à–µ–≥–æ —Ü–µ–ª–æ–≥–æ
print(tan1)
print(type (tan1)) # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ç–∏–ø–∞ –¥–∞–Ω–Ω—ã—Ö tan1
print()

# –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —É–≥–ª–∞ –∏–∑ –≥—Ä–∞–¥—É—Å–æ–≤ –≤ —Ä–∞–¥–∏–∞–Ω—ã
tan1_radians = math.radians(tan1)

# –í—ã—á–∏—Å–ª—è–µ–º x
x = d1 * math.tan(tan1_radians)
print(f'–†–µ–∑—É–ª—å—Ç–∞—Ç x = {x:.2f}')

# –í—ã—á–∏—Å–ª—è–µ–º –¥–ª–∏–Ω—É –ø—É—Ç–∏ L1
L1 = math.sqrt(x**2 + d1**2)
print()

# –í—ã—á–∏—Å–ª—è–µ–º –¥–ª–∏–Ω—É –ø—É—Ç–∏ L2
# –î–ª—è —ç—Ç–æ–≥–æ –≤–≤–µ–¥–µ–º —Å –∫–æ–Ω—Å–æ–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –±–æ–∫–æ–≤–æ–≥–æ —Å–º–µ—â–µ–Ω–∏—è –º–µ–∂–¥—É —Å–ø–∞—Å–∞—Ç–µ–ª–µ–º –∏ —É—Ç–æ–ø–∞—é—â–∏–º h (–≤ —è—Ä–¥–∞—Ö)
# –∏ d2 –∫—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —É—Ç–æ–ø–∞—é—â–µ–≥–æ –¥–æ –±–µ—Ä–µ–≥–∞
h = float(input('–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ h –≤ —è—Ä–¥–∞—Ö: '))
h = h * 3  # –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —è—Ä–¥–æ–≤ –≤ —Ñ—É—Ç—ã
print()
d2 = float(input('–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ d2 –≤ —Ñ—É—Ç–∞—Ö: '))
L2 = math.sqrt((h - x)**2 + d2**2)
print()

# –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç L1 –∏ L2
print(f'–†–µ–∑—É–ª—å—Ç–∞—Ç L1 = {(format(L1,'.2f'))}; L2 = {(format(L2,'.2f'))};')
print()

# –í–≤–µ–¥–µ–º —Å –∫–æ–Ω—Å–æ–ª–∏ —Å–∫–æ—Ä–æ—Å—Ç—å –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É vsand
# –∏ n –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∑–∞–º–µ–¥–ª–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø—Ä–∏ –¥–≤–∏–∂–µ–Ω–∏–∏ –≤ –≤–æ–¥–µ
vsand = float(input('–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ vsand –≤ –º–∏–ª—è—Ö –≤ —á–∞—Å: '))
vsand = vsand * 5280 / 3600  # –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –º–∏–ª—å –≤ —á–∞—Å –≤ —Ñ—É—Ç—ã –≤ —Å–µ–∫—É–Ω–¥—É
print()
print(f'–†–µ–∑—É–ª—å—Ç–∞—Ç –≤ —Ñ—É—Ç–∞—Ö –≤ —Å–µ–∫—É–Ω–¥—É vsand = {(format(vsand,'.2f'))}')
print()
n = float(input('–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ n: '))
print()

# –í—ã—á–∏—Å–ª—è–µ–º –æ–±—â–µ–µ –≤—Ä–µ–º—è t –Ω–∞ –¥–æ—Å—Ç–∏–∂–µ–Ω–∏–µ —É—Ç–æ–ø–∞—é—â–µ–≥–æ
t = (1 / vsand) * (L1 + n * L2)
print(f'–†–µ–∑—É–ª—å—Ç–∞—Ç –æ–±—â–µ–≥–æ –≤—Ä–µ–º–µ–Ω–∏ –¥–æ —É—Ç–æ–ø–∞—é—â–µ–≥–æ t = {(format(t,'.1f'))} —Å–µ–∫—É–Ω–¥')
print(type(t))